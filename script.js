const languageButtons = document.querySelectorAll(".lang-btn");
const translatedElements = document.querySelectorAll("[data-i18n]");
const translatedAriaElements = document.querySelectorAll("[data-i18n-aria-label]");
const menuButton = document.querySelector(".menu-button");
const navLinks = document.querySelector(".nav-links");
const contactForm = document.querySelector("#contact-form");
const inquiryLinks = document.querySelectorAll("[data-inquiry]");
const needSelect = contactForm.querySelector('select[name="need"]');
const languageField = contactForm.querySelector('input[name="language"]');
const submitButton = contactForm.querySelector('button[type="submit"]');
const submitLabel = submitButton.querySelector("[data-i18n]");
const formStatus = contactForm.querySelector(".form-status");
const descriptionMeta = document.querySelector('meta[name="description"]');
const ogTitleMeta = document.querySelector('meta[property="og:title"]');
const ogDescriptionMeta = document.querySelector('meta[property="og:description"]');
const twitterTitleMeta = document.querySelector('meta[name="twitter:title"]');
const twitterDescriptionMeta = document.querySelector('meta[name="twitter:description"]');
const ogLocaleMeta = document.querySelector('meta[property="og:locale"]');

function setLanguage(language) {
  const safeLanguage = translations[language] ? language : "en";
  const dictionary = translations[safeLanguage];
  document.documentElement.lang = safeLanguage;
  document.title = dictionary.metaTitle;
  descriptionMeta?.setAttribute("content", dictionary.metaDescription);
  ogTitleMeta?.setAttribute("content", dictionary.metaTitle);
  ogDescriptionMeta?.setAttribute("content", dictionary.metaDescription);
  twitterTitleMeta?.setAttribute("content", dictionary.metaTitle);
  twitterDescriptionMeta?.setAttribute("content", dictionary.metaDescription);
  const localeByLanguage = { en: "en_PL", pl: "pl_PL", tr: "tr_TR" };
  ogLocaleMeta?.setAttribute("content", localeByLanguage[safeLanguage]);

  translatedElements.forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  translatedAriaElements.forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (dictionary[key]) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === safeLanguage;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  const isMenuOpen = navLinks.classList.contains("open");
  menuButton.setAttribute("aria-label", isMenuOpen ? dictionary.menuClose : dictionary.menuOpen);
  languageField.value = safeLanguage;
  if (formStatus.classList.contains("success")) {
    formStatus.textContent = dictionary.formSuccess;
  } else if (formStatus.classList.contains("error")) {
    formStatus.textContent = dictionary.formError;
  }

  try {
    localStorage.setItem("zelta-language", safeLanguage);
  } catch {
    // The language switch still works when storage is unavailable.
  }
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

menuButton.addEventListener("click", () => {
  const isOpen = navLinks.classList.toggle("open");
  const dictionary = translations[document.documentElement.lang] || translations.en;
  menuButton.setAttribute("aria-expanded", String(isOpen));
  menuButton.setAttribute("aria-label", isOpen ? dictionary.menuClose : dictionary.menuOpen);
});

navLinks.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    navLinks.classList.remove("open");
    menuButton.setAttribute("aria-expanded", "false");
    const dictionary = translations[document.documentElement.lang] || translations.en;
    menuButton.setAttribute("aria-label", dictionary.menuOpen);
  });
});

document.addEventListener("keydown", (event) => {
  if (event.key !== "Escape" || !navLinks.classList.contains("open")) return;
  navLinks.classList.remove("open");
  menuButton.setAttribute("aria-expanded", "false");
  const dictionary = translations[document.documentElement.lang] || translations.en;
  menuButton.setAttribute("aria-label", dictionary.menuOpen);
  menuButton.focus();
});

window.addEventListener("resize", () => {
  if (window.innerWidth > 820 && navLinks.classList.contains("open")) {
    navLinks.classList.remove("open");
    menuButton.setAttribute("aria-expanded", "false");
  }
});

inquiryLinks.forEach((link) => {
  link.addEventListener("click", () => {
    needSelect.value = link.dataset.inquiry;
  });
});

contactForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  if (!contactForm.reportValidity()) return;

  const language = document.documentElement.lang;
  const dictionary = translations[language] || translations.en;
  const formData = new FormData(contactForm);

  submitButton.disabled = true;
  submitButton.setAttribute("aria-busy", "true");
  submitLabel.textContent = dictionary.formSending;
  formStatus.className = "form-status full-field";
  formStatus.textContent = "";

  try {
    const response = await fetch(contactForm.action, {
      method: "POST",
      body: formData,
      headers: {
        Accept: "application/json"
      }
    });

    if (!response.ok) {
      throw new Error(`Form submission failed with status ${response.status}`);
    }

    contactForm.reset();
    languageField.value = language;
    formStatus.classList.add("success");
    formStatus.textContent = dictionary.formSuccess;
  } catch {
    formStatus.classList.add("error");
    formStatus.textContent = dictionary.formError;
  } finally {
    submitButton.disabled = false;
    submitButton.removeAttribute("aria-busy");
    submitLabel.textContent = dictionary.formSubmit;
  }
});

document.querySelector("#year").textContent = new Date().getFullYear();

